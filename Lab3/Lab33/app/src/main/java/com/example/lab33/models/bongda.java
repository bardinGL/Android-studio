package com.example.lab33.models;

public class bongda {
    private int ava;
    private String name;
    private String info;
    private int flag;

   public bongda(int ava, String name, String info, int flag) {
        this.ava = ava;
        this.name = name;
        this.info = info;
        this.flag = flag;
    }

    public int getAva() {
        return ava;
    }

    public String getName() {
        return name;
    }

    public String getInfo() {
        return info;
    }

    public int getFlag() {
        return flag;
    }
}
